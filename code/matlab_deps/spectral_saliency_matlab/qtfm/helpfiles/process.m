     'cos' 'cosh' 'cross' 'ctranspose' 'cumprod' 'cumsum' ...
     'dc' 'det' 'diag' 'diff' 'disp' 'display' 'displayall' ...
     'eig' 'end' 'eq' 'exp' 'expm' 'eyeq' ...
     'fft' 'fft2' 'fftshift' 'fix' 'floor' 'fprintf' 'funm' ...
     'horzcat' ...
