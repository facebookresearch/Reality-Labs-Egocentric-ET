     'xyz' ...
     'zerosq' 'zerosv'};

S = 'xmlfiles/qtfmfunction.xsl';

h = waitbar(0, 'Processing XML to HTML helpfiles ...');

N = length(L);

for i = 1:N
    F = L{i};
    waitbar(i/N, h)
    xslt(['xmlfiles/', F, '.xml'], S, [F, '.html']);
end
close(h)

